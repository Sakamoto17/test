package tsd;

public class Sample2_7 {
	public static void main(String[] args) {
		int a = 10;
		int b = 2;

		System.out.println("a + b = " + ( a + b ));//　四則演算で文字列に吸収される
		System.out.println("a - b = " + ( a - b));
		System.out.println("a * b = " + (a * b));
		System.out.println("a / b = " + (a / b));
		System.out.println("a % b = " + (a % b));
		System.out.println("-a   = " + (-a));



	}

}
